import Foundation

public enum Tolerance {
    static let absolute: Double = pow(10, -15)
    static let relative: Double = pow(10, -15)
}

func +=<K, V> (inout left: Dictionary<K, V>, right: Dictionary<K, V>) -> Dictionary<K, V> {
    for (k, v) in right {
        left.updateValue(v, forKey: k)
    }
    return left
}